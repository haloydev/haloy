func TestHaloyConfig_MarshalYAML(t *testing.T) {
	config := &HaloyConfig{
		Name:   "test-app",
		Server: "api.example.com",
		Port:   "3000",
		Image: &ImageConfig{
			Repository: "test-app",
			Build:      true,
			BuildConfig: &BuildConfig{
				Context:    ".",
				Dockerfile: "Dockerfile",
			},
		},
		Env: []EnvVarConfig{
			{Name: "DATABASE_URL", From: &FromReference{Env: "DATABASE_URL"}},
		},
	}

	data, err := config.MarshalYAML()
	if err != nil {
		t.Fatalf("MarshalYAML() error = %v", err)
	}

	yaml := string(data)

	if !strings.Contains(yaml, "name: test-app") {
		t.Error("YAML should contain 'name: test-app'")
	}
	if !strings.Contains(yaml, "server: api.example.com") {
		t.Error("YAML should contain 'server: api.example.com'")
	}
	if !strings.Contains(yaml, "build: true") {
		t.Error("YAML should contain 'build: true'")
	}
}

func TestHaloyConfig_MarshalYAMLWithComments(t *testing.T) {
	config := &HaloyConfig{
		Name:   "test-app",
		Server: "api.example.com",
		Port:   "3000",
		Image: &ImageConfig{
			Repository: "test-app",
			Build:      true,
			BuildConfig: &BuildConfig{
				Context:    ".",
				Dockerfile: "Dockerfile",
			},
		},
		Env: []EnvVarConfig{
			{Name: "DATABASE_URL", From: &FromReference{Env: "DATABASE_URL"}},
			{Name: "API_KEY", From: &FromReference{Env: "API_KEY"}},
		},
	}

	data, err := config.MarshalYAMLWithComments()
	if err != nil {
		t.Fatalf("MarshalYAMLWithComments() error = %v", err)
	}

	yaml := string(data)

	checks := []struct {
		name     string
		contains string
	}{
		{"header comment", "# Haloy deployment configuration"},
		{"generated comment", "Generated by `haloy init`"},
		{"name field", "name: test-app"},
		{"server field", "server: api.example.com"},
		{"image section", "image:"},
		{"repository", "repository: test-app"},
		{"build config", "build_config:"},
		{"port field", "port: 3000"},
		{"domains comment", "# Uncomment and configure your domain"},
		{"env section", "env:"},
		{"env var name", "- name: DATABASE_URL"},
		{"env var from", "from:"},
		{"health check comment", "# health_check_path:"},
	}

	for _, check := range checks {
		t.Run(check.name, func(t *testing.T) {
			if !strings.Contains(yaml, check.contains) {
				t.Errorf("YAML should contain %q", check.contains)
			}
		})
	}
}

func TestHaloyConfig_MarshalYAMLWithComments_NoEnvVars(t *testing.T) {
	config := &HaloyConfig{
		Name:   "test-app",
		Server: "api.example.com",
		Port:   "3000",
		Image: &ImageConfig{
			Repository: "test-app",
			Build:      true,
		},
		Env: nil,
	}

	data, err := config.MarshalYAMLWithComments()
	if err != nil {
		t.Fatalf("MarshalYAMLWithComments() error = %v", err)
	}

	yaml := string(data)

	// Should have commented env example when no env vars
	if !strings.Contains(yaml, "# env:") {
		t.Error("YAML should contain commented '# env:' when no env vars")
	}
	if !strings.Contains(yaml, "#   - name: DATABASE_URL") {
		t.Error("YAML should contain commented env var example")
	}
}
