func TestSanitizeAppName(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"my-app", "my-app"},
		{"My App", "my-app"},
		{"my_app", "my-app"},
		{"MyApp", "myapp"},
		{"my.app.name", "myappname"},
		{"my@app!", "myapp"},
		{"  spaced  ", "spaced"},
		{"-leading-hyphen", "leading-hyphen"},
		{"trailing-hyphen-", "trailing-hyphen"},
		{"multiple---hyphens", "multiple-hyphens"},
		{"123app", "123app"},
		{"app123", "app123"},
		{"", "app"},
		{"@#$%", "app"},
		{"UPPERCASE", "uppercase"},
		{"MixedCase-App_Name", "mixedcase-app-name"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			result := sanitizeAppName(tt.input)
			if result != tt.expected {
				t.Errorf("sanitizeAppName(%q) = %q, want %q", tt.input, result, tt.expected)
			}
		})
	}
}

func TestGenerateHaloyConfig(t *testing.T) {
	envVars := []string{"DATABASE_URL", "API_KEY", "REDIS_URL"}

	config := GenerateHaloyConfig("/path/to/my-project", FrameworkNextJS, "api.example.com", envVars)

	if config.Name != "my-project" {
		t.Errorf("Name = %v, want my-project", config.Name)
	}
	if config.Server != "api.example.com" {
		t.Errorf("Server = %v, want api.example.com", config.Server)
	}
	if config.Port != "3000" {
		t.Errorf("Port = %v, want 3000", config.Port)
	}
	if config.Image == nil {
		t.Fatal("Image should not be nil")
	}
	if config.Image.Repository != "my-project" {
		t.Errorf("Image.Repository = %v, want my-project", config.Image.Repository)
	}
	if !config.Image.Build {
		t.Error("Image.Build should be true")
	}
	if len(config.Env) != 3 {
		t.Errorf("len(Env) = %v, want 3", len(config.Env))
	}
}

